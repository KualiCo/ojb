/**
 * Tests for the ojb.class tag with the refresh attribute.
 *
 * @author <a href="mailto:tomdz@users.sourceforge.net">Thomas Dudziak (tomdz@users.sourceforge.net)</a>
 */
public class ClassTagRefreshAttributeTests extends OjbTestBase
{
    public ClassTagRefreshAttributeTests(String name)
    {
        super(name);
    }

    // Test of refresh attribute: with no value
    public void testRefresh1()
    {
        addClass(
            "test.A",
            "package test;\n"+
            "/** @ojb.class refresh=\"\" */\n"+
            "public class A {}\n");

        assertNull(runOjbXDoclet(OJB_DEST_FILE));
        assertNull(runTorqueXDoclet(TORQUE_DEST_FILE, "ojbtest"));
    }

    // Test of refresh attribute: with invalid value
    public void testRefresh2()
    {
        addClass(
            "test.A",
            "package test;\n"+
            "/** @ojb.class refresh=\"no\" */\n"+
            "public class A {}\n");

        assertNull(runOjbXDoclet(OJB_DEST_FILE));
        assertNull(runTorqueXDoclet(TORQUE_DEST_FILE, "ojbtest"));
    }

    // Test of refresh attribute: with 'true' value
    public void testRefresh3()
    {
        addClass(
            "test.A",
            "package test;\n"+
            "/** @ojb.class refresh=\"true\" */\n"+
            "public class A {}\n");

        assertEqualsOjbDescriptorFile(
            "<class-descriptor\n"+
            "    class=\"test.A\"\n"+
            "    table=\"A\"\n"+
            "    refresh=\"true\"\n"+
            ">\n"+
            "</class-descriptor>",
            runOjbXDoclet(OJB_DEST_FILE));
        assertEqualsTorqueSchemaFile(
            "<database name=\"ojbtest\">\n"+
            "    <table name=\"A\">\n"+
            "    </table>\n"+
            "</database>",
            runTorqueXDoclet(TORQUE_DEST_FILE, "ojbtest"));
    }

    // Test of refresh attribute: with 'false' value
    public void testRefresh4()
    {
        addClass(
            "test.A",
            "package test;\n"+
            "/** @ojb.class refresh=\"false\" */\n"+
            "public class A {}\n");

        assertEqualsOjbDescriptorFile(
            "<class-descriptor\n"+
            "    class=\"test.A\"\n"+
            "    table=\"A\"\n"+
            "    refresh=\"false\"\n"+
            ">\n"+
            "</class-descriptor>",
            runOjbXDoclet(OJB_DEST_FILE));
        assertEqualsTorqueSchemaFile(
            "<database name=\"ojbtest\">\n"+
            "    <table name=\"A\">\n"+
            "    </table>\n"+
            "</database>",
            runTorqueXDoclet(TORQUE_DEST_FILE, "ojbtest"));
    }

}
